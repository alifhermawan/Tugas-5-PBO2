package pbo2.pkg10117045.latihan60.akatsuki;

public class Akatsuki {

    protected String nama;
    protected String asal;
    protected String posisiCincin;
    protected String jutsu;

    public Akatsuki(String nama, String asal, String posisiCincin, String jutsu) {
        this.nama = nama;
        this.asal = asal;
        this.posisiCincin = posisiCincin;
        this.jutsu = jutsu;

    }

    public String getNama() {
        return nama;
    }

    public String getAsal() {
        return asal;
    }

    public String getPosisiCincin() {
        return posisiCincin;
    }

    public String getJutsu() {
        return jutsu;
    }

}
